#Operator Aritmatika
numberOne = 10
numberTwo = 3

modResult = numberOne % numberTwo
pangkatResult = numberOne ** numberTwo
bilanganBulatResult = numberOne // numberTwo
bilanganCacahResult = numberOne / numberTwo
print(modResult, pangkatResult,bilanganBulatResult,bilanganCacahResult)

#Operator Perbandingan
resultKurangDari = numberOne < numberTwo
resultSamaDengan = numberOne == numberTwo
resultLebihDari = numberOne > numberTwo
print(resultKurangDari, resultSamaDengan, resultLebihDari)